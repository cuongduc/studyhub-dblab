<h2>Our Approach</h2>
<p><strong>The StudyHub experience</strong></p>
It&lsquo;s simple. We want to help students learn better and faster. That&lsquo;s why we&lsquo;ve designed our platform based on proven teaching methods verified by top researchers.
Here are 4 key ideas that were influential in shaping our vision:

<p><strong>Effectiveness of online learning</strong></p>
Online learning plays a significant role in a lifelong education. In fact, a recent report by the U.S. Department of Education found that “classes with online learning (whether taught completely online or blended) on average produce stronger student learning outcomes than do classes with solely face-to-face instruction.”

<p><strong>Mastery learning</strong></p>
Based on an approach developed by educational psychologist Benjamin Bloom, Mastery Learning helps students fully understand a topic before moving onto a more advanced topic. On StudyHub, we typically give immediate feedback on a concept a student did not understand. In many cases, we provide randomized versions of the assignment so a student can re-study and re-attempt the homework.

<p><strong>Peer assessments</strong></p>
In many courses, the most meaningful assignments cannot be easily graded by a computer. That&lsquo;s why we use peer assessments, where students can evaluate and provide feedback on each other&lsquo;s work. This technique has been shown in many studies to result in accurate feedback for the student and a valuable learning experience for the grader.

<p><strong>Blended learning</strong></p>
Many of our partner institutions are using our online platform to provide their on-campus students with an improved learning experience. This blended model of learning has shown in studies to increase student engagement, attendance and performance.



